public class Children {
    private String name;
    private String birthdate;

    public Children(String name, String birthdate){
        this.name = name;
        this.birthdate = birthdate;
    }

    public String getName() {
        return name;
    }

    public String getBirthday() {
        return birthdate;
    }

}
